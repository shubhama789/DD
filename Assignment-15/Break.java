public class Break{
	public static void main(String args[]){
		for(int x=0;x<10;x++)
	{	
		switch(x)
		{
		case 0:
		System.out.println("\nThe value of x is 0");
		break;
		case 1:
		System.out.println("\nThe value of x is 1");
		break;
		case 2:
		System.out.println("\nThe value of x is 2");
		break;
		case 3:
		System.out.println("\nThe value of x is 3");
		break;
		case 4:
		System.out.println("\nThe value of x is 4");
		break;
		default:
		System.out.println("\nThe value of x is less than 10");
		}
	}
}
}